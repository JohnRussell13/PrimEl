void main(void){
    //init notes
    init_notes();
    
    //init used pins
    TRISBbits.TRISB12 = 1;//MQ-3
    TRISAbits.TRISA11 = 0;//BUZZER
    TRISBbits.TRISB11 = 0;//SERVO
    ADPCFGbits.PCFG11 = 1;
    TRISDbits.TRISD8 = 1;//PIR
    TRISFbits.TRISF6 = 0;//mode checker
    
    //timer2 init
    Init_T2();

    //GLCD init
	ConfigureLCDPins();
	ConfigureTSPins();
	GLCD_LcdInit();
	GLCD_ClrScr();
    
    //UART1 init
    initUART1();
	
    //ADC init
	ADCinit();
	ConfigureADCPins();
	ADCON1bits.ADON = 1;
    while(1)
    {
        if(raw2 > (V_MAX >> 2)){//MQ3 over 25%
            if(raw2 > (V_MAX >> 1)){//MQ3 over 50%
                if(raw2 > (V_MAX >> 1) + (V_MAX >> 2)){//MQ3 over 75%
                    dies_irae();//play requiem - dies irae
                }
                else{//MQ3 between 50% and 75%
                    play_gif();//show animated symbol for escape
                }
            }
            if(door_f == 0){//if closed - open
                door(OPEN_DOOR);
                door_f = 1;
            }
        }
        else{//MQ3 under 25%
            GLCD_DisplayPicture(MENU);//show menu on GLCD
            if(mode == 0){//check for mode
                play_piano();
            }
            else if(mode == 1){
                play_jukebox();
            }
            if(buff[n - 1] == ';'){//check for UART command (ends in ;)
                if(buff[0] == 'c' && buff[1] == 'l' && buff[2] == 'o' && buff[3] == 's' && buff[4] == 'e' && buff[5] == ';'){//if close; try to close them
                    n = 0;
                    buff[1] = 0;
                    if(door_f == 1){//if opened - close
                        door(CLOSE_DOOR);
                        RS232_putst("Closing door.\n");
                        door_f = 0;
                    }
                    else{//if closed - send over UART
                        RS232_putst("Already closed.\n");
                    }
                }
                else if(buff[0] == 'o' && buff[1] == 'p' && buff[2] == 'e' && buff[3] == 'n' && buff[4] == ';'){//if close; try to close them
                    n = 0;
                    buff[1] = 0;
                    if(door_f == 0){//if opened - close
                        door(OPEN_DOOR);
                        RS232_putst("Opening door.\n");
                        door_f = 1;
                    }
                    else{//if closed - send over UART
                        RS232_putst("Already opened.\n");
                    }
                }
                else{//for other commands
                    RS232_putst("Wrong command.\n");
                    n = 0;
                }
            }
            //automatic door close, no need for differentiation
            if(PORTDbits.RD8 == 1 && door_f == 1){//check for PIR and try to close
                door(CLOSE_DOOR);
                RS232_putst("Closing door.\n");
                door_f = 0;
            }
        }
    }
    return 0;
}
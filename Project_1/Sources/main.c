//init UART1 
void initUART1(void){
    U1BRG = 0x0040;//baud rate 9600
    U1MODEbits.ALTIO = 0;//don't use alt pin
    IEC0bits.U1RXIE = 1;//interrupt enable
    U1STA &= 0xfffc;//buffer is empty
    U1MODEbits.UARTEN = 1;//enable UART and set pins for UART accordingly
    U1STAbits.UTXEN = 1;//enable transmission
}
//UART1 interrupt
void __attribute__((__interrupt__)) _U1RXInterrupt(void) {
    IFS0bits.U1RXIF = 0;//flag down
    buff[n++] = U1RXREG;//read into buffer
} 
//send ASCII over UART1
void WriteUART1(unsigned int data){
	while(U1STAbits.TRMT == 0);//wait while transmit register is full
    if(U1MODEbits.PDSEL == 3)//check for parity bit
        U1TXREG = data;
    else
        U1TXREG = data & 0xFF;
}
//send string over UART1
void RS232_putst(register const char *str){
    while((*str)!=0){
        WriteUART1(*str);
        if (*str==13) WriteUART1(10);//make sure proper \n is added
        if (*str==10) WriteUART1(13);//make sure proper \n is added
        str++;
    }
}
//OG function for for printing numbers of unknown length
void WriteUART1dec2string(unsigned long data){
	j = 1;
    //break our input number into nice chunks
    while(1){
        if(data / j < 10){
            break;
        }
        j = j * 10;
    }
    //print those chunks
    i = data / j;
    while(i >= 0 && i < 10){
        WriteUART1('0' + i);
        data = data % j;
        j = j / 10;
        i = data / j;
    }
}
